class CardDisplayComp : public DisplayComponent
{
    const Card* m_card = nullptr;

protected:
    void OnDisplay() override
    {
        if (m_card)
        {
            OnDisplayCard(*m_card);
        }
    }

    virtual void OnDisplayCard(const Card& card) = 0;

public:
    virtual ~CardDisplayComp() = default;

    void DisplayCard(const Card& card)
    {
        m_card = &card;
        Display();
        m_card = nullptr;
    }
};

